public class Report {
    private String id;
    private String gradYear;
    private String mhi;
    private String mhiText;
    private String date;
    private String identityYN;
    private String identityText;
    private String location;
    private String eventDes;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGradYear() {
        return gradYear;
    }

    public void setGradYear(String gradYear) {
        this.gradYear = gradYear;
    }

    public String getMHI() {
        return mhi;
    }

    public void setMHI(String mhi) {
        this.mhi = mhi;
    }

    public String getMHIText() {
        return mhiText;
    }

    public void setMHIText(String mhiText) {
        this.mhiText = mhiText;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getIdentityYN() {
        return identityYN;
    }

    public void setIdentityYN(String identityYN) {
        this.identityYN = identityYN;
    }

    public String getIdentityText() {
        return identityText;
    }

    public void setIdentityText(String identityText) {
        this.identityText = identityText;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getEventDes() {
        return eventDes;
    }

    public void setEventDes(String eventDes) {
        this.eventDes = eventDes;
    }
}
